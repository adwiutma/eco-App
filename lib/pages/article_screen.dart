Materi tentang sampah sangat penting untuk di ketahui. Selama manusia masih beraktivitas, sampah menjadi salah satu hal yang akan konsisten dihasilkan dan terus bertambah. Untuk itu diperlukan [upaya penanganan sampah](https://waste4change.com/blog/daur-ulang-sampah/) secara tepat dan bertanggung jawab agar volume sampah dapat dikurangi dan mencegahnya menumpuk mencemari lingkungan.

